def positivos(numeros):
    resultado_positivos = numeros > 0
    return resultado_positivos
    
def negativos(numeros):
    resultado_negativos = numeros < 0
    return resultado_negativos

def es_par(numeros):
    resultado_es_par = numeros % 2 == 0
    return resultado_es_par


def es_impar(numeros):
    resultado_es_impar = numeros % 2 != 0
    return resultado_es_impar

